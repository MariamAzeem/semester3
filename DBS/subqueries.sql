--Employees with same job as emp 7369 and salary > emp 7876
SELECT *
FROM EMP
WHERE JOB = (SELECT JOB FROM EMP WHERE EMPNO = 7369)
  AND SAL > (SELECT SAL FROM EMP WHERE EMPNO = 7876);
 
--Departments having minimum salary greater than that of department 50
SELECT DEPTNO
FROM EMP
GROUP BY DEPTNO
HAVING MIN(SAL) > (SELECT MIN(SAL) FROM EMP WHERE DEPTNO = 50);
 
--Job with the lowest average salary
SELECT JOB
FROM EMP
GROUP BY JOB
HAVING AVG(SAL) = (
    SELECT MIN(AVG(SAL))
    FROM EMP
    GROUP BY JOB
);
 
--employees whose salary is less than any CLERK and who are not CLERK
SELECT *
FROM EMP
WHERE SAL < ANY (SELECT SAL FROM EMP WHERE JOB = 'CLERK')
  AND JOB <> 'CLERK';
 
--Employees who earn same salary as the minimum salary of departments
SELECT *
FROM EMP
WHERE SAL IN (
    SELECT MIN(SAL)
    FROM EMP
    GROUP BY DEPTNO
);
